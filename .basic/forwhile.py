# 반복문

# for 반복문은 시퀀스 자료형(리스트, 튜플, 문자열 등)의 요소를 하나씩 꺼내서 변수에 저장한 후, 블록을 실행하는 구조입니다.
# 기본 구조:
# for 변수명 in 범위:
#     실행할 코드
# 예시 1: 리스트의 요소를 출력하는 for 반복문
fruits = ['apple', 'banana', 'cherry']
for fruit in fruits:
    print(fruit)    # apple
                    # banana
                    # cherry
                    
# 예시 2: 문자열의 각 문자를 출력하는 for 반복문
word = "hello"
for letter in word:
    print(letter)   # h
                    # e
                    # l
                    # l
                    # o
                    
# 예시 3: range() 함수를 사용한 for 반복문
# range() 함수는 지정된 범위의 숫자를 생성하는 함수입니다. range(start, stop, step) 형태로 사용됩니다. start는 시작 숫자, stop은 끝 숫자(포함되지 않음), step은 증가 간격입니다. range(시작, 끝, 스텝) 형태로 사용됩니다. 시작과 스텝은 생략할 수 있으며, 생략하면 시작은 0, 스텝은 1이 됩니다. 예를 들어, range(5)는 0부터 4까지의 숫자를 생성하고, range(1, 11, 2)는 1부터 10까지의 홀수를 생성합니다. 시작은 포함하고, 끝은 포함하지 않는다는 점에 유의해야 합니다. 따라서, range(5)는 0, 1, 2, 3, 4를 생성하고, range(1, 11, 2)는 1, 3, 5, 7, 9를 생성합니다.

# 예시 3-1: 0부터 4까지의 숫자를 출력하는 for 반복문
for i in range(5):
    print(i)    # 0
                # 1
                # 2
                # 3
                # 4
# 예시 3-2: 1부터 10까지의 홀수를 출력하는 for 반복문
for i in range(1, 11, 2):
    print(i)    # 1
                # 3
                # 5
                # 7
                # 9

