#연산자
#연산자 우선 순위: 거듭제곱>곱셉,나눗셈,나머지>덧셈,뺄셈>비교연산자>논리연산자

#산술연산자, 비교연산자, 논리연산자, 할당연산자 
#산술연산자: +(덧셈), -(뺄셈), *(곱셈), /(나눗셈), %(나머지), **(거듭제곱)
#조건식에 산술연산자를 썼을 때, 0이 아니면 참, 0이면 거짓으로 판단한다.

#비교연산자: ==(같다), !=(같지않다), >(크다), <(작다), >=(크거나 같다), <=(작거나 같다)
#조건식에 비교연산자를 썼을 때, 참이면 True, 거짓이면 False로 판단한다.

#논리연산자: and(그리고), or(또는), not(부정)
#조건식에 논리연산자를 썼을 때, 참과 거짓을 판단한다.
# and: 둘 다 참이면 참, 하나라도 거짓이면 거짓
# or: 둘 다 거짓이면 거짓, 하나라도 참이면 참
# not: 참이면 거짓, 거짓이면 참

#할당연산자: =(대입), +=(더해서 대입), -=(빼서 대입), *=(곱해서 대입), /=(나눠서 대입), %=(나머지를 대입), **=(거듭제곱해서 대입)
#할당연산자는 변수에 값을 대입할 때 사용한다. 예를 들어, x = 5는 x에 5를 대입하는 것이다. x += 3은 x에 3을 더해서 다시 x에 대입하는 것이다. 즉, x = x + 3과 같다.

# = 와 == 의 차이: = 는 대입 연산자이고, == 는 비교 연산자이다. = 는 변수에 값을 대입하는 데 사용되고, == 는 두 값이 같은지를 비교하는 데 사용된다. 예를 들어, x = 5는 x에 5를 대입하는 것이고, x == 5는 x가 5와 같은지를 비교하는 것이다. 좀 더 구체적으로 설명하자면, x = 5는 x라는 변수에 5라는 값을 저장하는 것이고, x == 5는 x라는 변수에 저장된 값이 5와 같은지를 확인하는 것이다. 따라서, x = 5는 참이지만, x == 5는 참이 될 수도 있고 거짓이 될 수도 있다.

#이러한 연산자를 배우는 이유는 조건문과 반복문에서 조건식을 작성할 때 사용하기 때문이다. 조건식은 프로그램의 흐름을 제어하는 데 중요한 역할을 한다. 예를 들어, if 문에서 조건식을 작성할 때 비교 연산자와 논리 연산자를 사용하여 조건을 설정할 수 있다. 또한, 반복문에서도 조건식을 사용하여 반복을 제어할 수 있다. 따라서, 연산자를 잘 이해하고 사용하는 것은 프로그래밍에서 매우 중요하다.

#조건문에서는 if, elif, else를 사용하여 조건을 설정할 수 있다. if 문은 조건이 참일 때 실행되는 블록을 정의하고, elif 문은 이전 조건이 거짓이고 현재 조건이 참일 때 실행되는 블록을 정의하며, else 문은 모든 이전 조건이 거짓일 때 실행되는 블록을 정의한다. 

if 조건식1:
    실행명령1
    실행명령2
    
#띄어쓰기가 중요하다. 조건식이 참일 때 실행되는 명령들은 들여쓰기를 해야 한다. 들여쓰기는 보통 4칸의 공백을 사용한다. 들여쓰기가 잘못되면 SyntaxError가 발생한다.

if 조건식1:
    실행명령1
    실행명령2
elif 조건식2:
    실행명령3
    실행명령4
    
#elif는 if문 다음에 여러 개 사용할 수 있다. elif는 이전 조건이 거짓이고 현재 조건이 참일 때 실행되는 블록을 정의한다. elif는 else보다 먼저 작성해야 한다. else는 모든 이전 조건이 거짓일 때 실행되는 블록을 정의한다. else는 if문과 elif문 다음에 작성해야 한다. 

if 조건식1:
    실행명령1
    실행명령2
elif 조건식2:
    실행명령3
    실행명령4
else:
    실행명령5
    실행명령6
    
#else는 위에있는 모든 조건이 거짓일 때 실행되는 블록을 정의한다. else는 if문과 elif문 다음에 작성해야 한다. else는 조건식이 없기 때문에, else 블록은 항상 실행된다. 따라서, else 블록은 프로그램의 흐름을 제어하는 데 중요한 역할을 한다.  중간에 elif는 여러개 있을 수 있다.

if 조건식1:
    if 조건식2:
        실행명령1
    elif 조건식2:
        실행명령2
    else:
        실행명령3
else:
    실행명령4
    
#조건문은 중첩해서 사용할 수 있다. 중첩된 조건문은 들여쓰기를 통해 구분한다. 중첩된 조건문은 프로그램의 흐름을 더욱 세밀하게 제어할 수 있게 해준다.       


# 조건문
score = 90
if score >= 90:
    print("A")
elif score >= 60:
    print("B")
else:
    print("C")    
    
# 조건문을 사용하여 점수에 따라 학점을 출력하는 예제이다. score가 90 이상이면 "A"를 출력하고, score가 60 이상이면 "B"를 출력하며, 그 외의 경우에는 "C"를 출력한다. 이 예제에서는 elif를 사용하여 여러 조건을 처리하고 있다.

