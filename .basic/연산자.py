#연습해보기2. 날짜가 짝수면 "짝수번호 차량 통행 가능"을 출력하고, 날짜가 홀수면 "홀수번호 차량 통행 가능"을 출력하라.

date = '2026-03-16'
print(date.split('-')) # ['2026', '03', '16']. 그러므로 date.split('-')[2]를 사용하여 날짜를 가져올 수 있다. date.split('-')[2]는 '16'이 된다. 따라서, date.split('-')[2]를 정수로 변환하여 짝수인지 홀수인지 판단할 수 있다.

day=int(date.split('-')[2]) #여기서 int 해주는 이유는, date.split('-')[2]는 문자열이기 때문에, 정수로 변환하여 짝수인지 홀수인지 판단하는 연산을 해야 하기 때문이다. 예를 들어, '16'은 문자열이지만, 16은 정수이다. 따라서, day 변수에는 정수 16이 저장된다. 이제 day 변수를 사용하여 짝수인지 홀수인지 판단할 수 있다.
if day % 2 == 0:
    print("짝수번호 차량 통행 가능")
elif day % 2 == 1:
    print("홀수번호 차량 통행 가능")
    
# % 연산자를 사용하여 짝수와 홀수를 판단할 수 있다. day % 2 == 0이면 짝수이고, day % 2 == 1이면 홀수이다. 그 이유는, 짝수는 2로 나누었을 때 나머지가 0이기 때문이다. 예를 들어, 16은 짝수이므로 16 % 2 == 0이 참이 된다. 반면에, 홀수는 2로 나누었을 때 나머지가 1이기 때문이다. 예를 들어, 15는 홀수이므로 15 % 2 == 1이 참이 된다.
# 따라서, 조건문을 사용하여 날짜가 짝수인지 홀수인지 판단하고, 그에 따라 출력할 수 있다. 


# 연습문제 3. 구매금액 + 배송비(3,000원), 50,000원 이상은 무료배송이다. 총 결제 금액을 출력하라. 단 elif, else 사용없이 if는 하나만 사용한다. 

price = 25000
if price < 50000:
    price = price + 3000 # 배송비를 구매금액에 더한다. price = price + 3000은 price에 3000을 더해서 다시 price에 대입하는 것이다. 즉, price += 3000과 같다. 만약 price가 25000이라면, price는 28000이 된다. 만약 price가 50000 이상이라면, 배송비를 더하지 않고 그대로 price를 유지한다. 따라서, if 문을 사용하여 가격이 50000 미만일 때만 배송비를 추가하는 것이다.
    
print(f'결제금액: {price}') 