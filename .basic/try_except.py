#raise 명령어
# raise 명령어는 예외를 강제로 발생시키는 데 사용된다. raise를 이용하여 특정 조건에서 예외를 발생시키거나, 사용자 정의 예외를 만들 수 있다. 예를 들어, 다음과 같이 사용할 수 있다.
def set_age(age):
    if age < 0:
        raise ValueError("나이는 0 이상이어야 합니다.")
    print(f"나이는 {age}입니다.") 

print(set_age(-3)) # 이 코드는 ValueError를 발생시키며, "나이는 0 이상이어야 합니다."라는 메시지를 출력한다. raise 명령어를 사용하여 예외를 강제로 발생시킴으로써, 프로그램의 흐름을 제어하고, 특정 조건에서 예외가 발생하도록 할 수 있다. 또한, 사용자 정의 예외 클래스를 만들어서 더 구체적인 예외 처리를 할 수도 있다.

try:
    x = -5
    if x < 0:
        raise ValueError("x는 음수일 수 없습니다.")
except ValueError as e:
    print(f"예외가 발생했습니다: {e}")
# 위 코드에서, x가 음수이므로 ValueError가 발생한다. except 블록에서 해당 예외를 처리하여 "예외가 발생했습니다: x는 음수일 수 없습니다."라는 메시지를 출력한다. 이렇게 하면, raise 명령어를 사용하여 예외를 강제로 발생시키고, except 블록에서 해당 예외를 처리하여 프로그램이 중단되지 않고 안전하게 실행될 수 있도록 할 수 있다.