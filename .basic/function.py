# 함수가 받아야하는 매개변수는 앞에, 기본값이 있는 매개변수는 뒤에 배치하는 것이 일반적이다. 이렇게 하면, 함수를 호출할 때 기본값이 있는 매개변수를 생략할 수 있다. 예를 들어, 다음과 같이 함수를 정의할 수 있다.
def introduce(name, age, city="서울"):
    print(f"안녕하세요, 저는 {name}입니다. 나이는 {age}살이고, {city}에 살고 있습니다.")
introduce("성현", 25) # 안녕하세요, 저는 성현입니다. 나이는 25살이고, 서울에 살고 있습니다.
introduce("민수", 30, "부산") # 안녕하세요, 저는 민수입니다. 나이는 30살이고, 부산에 살고 있습니다. 

def avg(*numbers):
    total = sum(numbers) # sum은 숫자의 합계를 계산하는 함수이다.
    count = len(numbers) # len은 숫자의 개수를 계산하는 함수이다.
    return total / count if count > 0 else 0

# 가변 매개변수 *numbers는 함수가 호출될 때 전달된 모든 숫자들을 튜플 형태로 받아서 처리한다. sum(numbers)를 사용하여 숫자들의 합계를 계산하고, len(numbers)를 사용하여 숫자의 개수를 계산한다. 마지막으로, 평균을 계산하여 반환한다. 만약 숫자가 전달되지 않은 경우에는 0을 반환한다.
print(avg(1, 2, 3, 4, 5,6)) # 3.5
print(avg(10, 20)) # 15.0  
print(avg()) # 0

