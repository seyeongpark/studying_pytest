# 매개변수의 기본값 미리 지정 하는 경우
# 함수를 호출할 때, 매개ㅐ변수의 순서에 맞추어서 전달 할 수있다. 
# 매개 변수명을 지정하면, 매개변수의 순서에 상관없이 호출할 수 있다. 매개변수에 기본값을 지정하면, 함수를 호출할 때 해당 매개변수를 생략할 수 있다. 생략된 매개변수는 기본값으로 대체된다.
def greet(name, greeting="안녕하세요"):
    return f"{greeting}, {name}!"

print(greet("성현")) # 안녕하세요, 성현!
print(greet("민수", "안녕")) # 안녕, 민수!
