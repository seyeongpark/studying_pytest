# 튜플 생성
t1 = () # 비어있는 튜플 생성
t2 = (1, 2, 3) # 정수 요소를 가지는 튜플 생성
t3 = 1, 2, 3 # 괄호 없이도 튜플 생성 가능
t4 = (1, ) # 요소가 하나인 튜플 생성 (콤마 필요). 크게 의미는 없지만, 튜플을 생성할 때 요소가 하나인 경우에는 콤마를 붙여야 한다. 예를 들어, t = (1, )는 요소가 하나인 튜플을 생성하지만, t = (1)이라고 하면 정수 1이 될 뿐 튜플이 되지 않는다. 따라서, 요소가 하나인 튜플을 생성할 때는 콤마를 붙이는 것이 중요하다. 
t5 = (1, 'apple', 3.14, True) # 다양한 데이터 타입의 요소를 가지는 튜플 생성

# 튜플은 리스트와 달리 변경할 수 없는(immutable) 자료형이다. 따라서, 튜플의 요소를 수정하거나 삭제할 수 없다. 예를 들어, t[0] = 10과 같이 튜플의 요소를 수정하려고 하면 TypeError가 발생한다. 또한, t.append(4)와 같이 튜플에 요소를 추가하려고 하면 AttributeError가 발생한다. 튜플은 변경할 수 없는 자료형이기 때문에, 요소를 수정하거나 삭제할 수 없다. 따라서, 튜플을 사용할 때는 요소를 변경할 필요가 없는 경우에 사용하는 것이 좋다.

print(t5[0]) # 1
t5[0] = 10 # TypeError: 'tuple' object does not support item assignment
# 튜플의 요소를 수정하려고 하면 TypeError가 발생한다. 예를 들어, t5[0] = 10과 같이 튜플의 요소를 수정하려고 하면 'tuple' object does not support item assignment이라는 오류가 발생한다. 이는 튜플이 변경할 수 없는 자료형이기 때문에, 요소를 수정할 수 없다는 것을 의미한다. 따라서, 튜플을 사용할 때는 요소를 변경할 필요가 없는 경우에 사용하는 것이 좋다.

# 튜플을 주로 쓰는 경우는, 요소의 변경이 필요 없는 경우나, 리스트보다 더 빠르게 접근할 수 있는 자료형이 필요한 경우이다. 예를 들어, 함수의 반환값으로 여러 개의 값을 반환할 때 튜플을 사용할 수 있다. 또한, 튜플은 리스트보다 더 빠르게 접근할 수 있기 때문에, 요소의 변경이 필요 없는 경우에는 튜플을 사용하는 것이 좋다.

fruit = ('apple', 'banana', 'cherry') # 튜플 생성
a,b,c =fruit # '튜플 언패킹' 이라고 한다. 튜플 언패킹은 튜플의 요소를 각각의 변수에 할당하는 것을 말한다. 예를 들어, a, b, c = fruit과 같이 튜플 언패킹을 하면, fruit 튜플의 첫 번째 요소인 'apple'이 변수 a에 할당되고, 두 번째 요소인 'banana'가 변수 b에 할당되며, 세 번째 요소인 'cherry'가 변수 c에 할당된다. 튜플 언패킹은 튜플의 요소를 각각의 변수에 쉽게 할당할 수 있게 해주는 편리한 기능이다. [튜플 시각화 표 첨부]

# 값이 어떻게 다른지 보자
print(a,b,c) # 'apple' 'banana' 'cherry'
a, *b = fruit # a에는 'apple'이 할당되고, b에는 ['banana', 'cherry']가 할당된다. 튜플 언패킹에서 *를 사용하면, 나머지 요소들을 리스트로 묶어서 변수에 할당할 수 있다. 예를 들어, a, *b = fruit과 같이 하면, fruit 튜플의 첫 번째 요소인 'apple'이 변수 a에 할당되고, 나머지 요소인 'banana'와 'cherry'가 리스트 ['banana', 'cherry']로 묶여서 변수 b에 할당된다. 따라서, a는 'apple'이 되고, b는 ['banana', 'cherry']가 된다. 튜플 언패킹에서 *를 사용하면, 나머지 요소들을 리스트로 묶어서 변수에 할당할 수 있기 때문에 편리하다. 
print(a,b) # 'apple' ['banana', 'cherry']

# 튜플은 리스트와 기능은 비슷한데, 튜플은 괄호를 사용하여 생성하고, 변경할 수 없는 자료형이라는 점에서 차이가 있다. 튜플은 리스트보다 더 빠르게 접근할 수 있기 때문에, 요소의 변경이 필요 없는 경우에는 튜플을 사용하는 것이 좋다. 또한, 튜플 언패킹을 사용하여 튜플의 요소를 각각의 변수에 쉽게 할당할 수 있다. 튜플은 리스트와 기능은 비슷하지만, 변경할 수 없는 자료형이라는 점에서 차이가 있다. 따라서, 튜플을 사용할 때는 요소를 변경할 필요가 없는 경우에 사용하는 것이 좋다.