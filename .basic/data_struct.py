# 리스트 생성
list1 = [1, 2, 3, 4, 5] # 리스트는 대괄호 []로 감싸서 표현한다. 리스트는 여러 개의 값을 순서대로 저장할 수 있는 자료형이다. 예를 들어, [1, 2, 3, 4, 5]는 1부터 5까지의 정수를 저장하는 리스트이다. 
list2 = ['apple', 'banana', 'cherry'] # 리스트는 다양한 데이터 타입의 값을 저장할 수 있다. 예를 들어, ['apple', 'banana', 'cherry']는 문자열을 저장하는 리스트이다.

# 두 list의 공통점은 ,을 이용하여 요소를 구분한다는 것이다. 리스트의 요소는 순서가 있으며, 인덱스를 이용하여 특정 위치의 요소에 접근할 수 있다. 예를 들어, list1[0]은 1을 반환하고, list2[1]은 'banana'를 반환한다.

#장점은 여러 타입의 데이터를 저장할 수 있다는 것이다. 예를 들어, list3 = [1, 'apple', 3.14, True]는 정수, 문자열, 실수, 불리언 값을 모두 저장하는 리스트이다. 리스트는 다양한 데이터 타입의 값을 저장할 수 있기 때문에 유용하다. 

list3 = [1, 'apple', 3.14, True] # 리스트는 다양한 데이터 타입의 값을 저장할 수 있다. 예를 들어, [1, 'apple', 3.14, True]는 정수, 문자열, 실수, 불리언 값을 모두 저장하는 리스트이다. 리스트는 다양한 데이터 타입의 값을 저장할 수 있기 때문에 유용하다.

# 다른 형태가 들어갈 수 있으므로 array보다 유연하다. 예를 들어, list4 = [1, [2, 3], 'hello', {'key': 'value'}]는 정수, 리스트, 문자열, 딕셔너리를 모두 저장하는 리스트이다. 리스트는 다양한 형태의 데이터를 저장할 수 있기 때문에 유연하다.

# 리스트는 순서를 가지고있다. 예를 들어, list5 = ['first', 'second', 'third']는 'first'가 첫 번째 요소, 'second'가 두 번째 요소, 'third'가 세 번째 요소인 리스트이다. 리스트는 순서를 가지고 있기 때문에, 요소의 위치에 따라 다른 값을 저장할 수 있다. 즉 first는 0번째 요소, second는 1번째 요소, third는 2번째 요소이다. 리스트는 순서를 가지고 있기 때문에, 요소의 위치에 따라 다른 값을 저장할 수 있다. 따라서, list5[0]은 'first'를 반환하고, list5[1]은 'second'를 반환하며, list5[2]는 'third'를 반환한다.

#리스트 요소가 리스트일 경우 중첩 리스트라고 한다. 예를 들어, list6 = [1, [2, 3], 4]는 1과 4라는 정수 요소와 [2, 3]이라는 리스트 요소를 포함하는 중첩 리스트이다. 중첩 리스트는 리스트 안에 또 다른 리스트가 포함된 형태의 리스트이다. 중첩 리스트는 리스트 안에 또 다른 리스트가 포함된 형태의 리스트이다. 따라서, list6[0]은 1을 반환하고, list6[1]은 [2, 3]을 반환하며, list6[2]는 4를 반환한다. 또한, list6[1][0]은 2를 반환하고, list6[1][1]은 3을 반환한다.


#요소 추가하기 : 리스트.append(추가할요소) 
list7 = [1, 2, 3] # 리스트 생성
list7.append(4) # 리스트에 요소 추가
print(list7) # [1, 2, 3, 4]
# 리스트.append(추가할요소) 메서드는 리스트의 끝에 새로운 요소를 추가한다. 예를 들어, list7.append(4)는 list7 리스트의 끝에 4를 추가하여 [1, 2, 3, 4]라는 새로운 리스트를 만든다. 리스트.append() 메서드는 원래의 리스트를 변경하므로, list7은 이제 [1, 2, 3, 4]가 된다. 리스트.append() 메서드는 리스트에 요소를 추가할 때 유용하다.

#리스트.insert(위치, 추가할요소) : 리스트의 특정 위치에 요소를 추가한다. 예를 들어, list8 = [1, 2, 4]라는 리스트가 있을 때, list8.insert(2, 3)을 호출하면 list8 리스트의 인덱스 2에 3이 추가되어 [1, 2, 3, 4]라는 새로운 리스트가 된다. 리스트.insert() 메서드는 리스트의 특정 위치에 요소를 추가할 때 유용하다. 리스트.insert() 메서드는 원래의 리스트를 변경하므로, list8은 이제 [1, 2, 3, 4]가 된다. 리스트.insert() 메서드는 리스트에 요소를 추가할 때 유용하다.
list8 = [1, 2, 4] # 리스트 생성
list8.insert(2, 3) # 리스트의 특정 위치에 요소 추가
print(list8) # [1, 2, 3, 4]

#예시
list9 = ['SP', 'John', 'Andy']
list9.append('Tom') # 리스트의 끝에 'Tom' 추가
print(list9) # ['SP', 'John', 'Andy', 'Tom']

list9.insert(1, 'Alice') # 리스트의 인덱스 1에 'Alice' 추가
print(list9) # ['SP', 'Alice', 'John', 'Andy', 'Tom']

#insert() 메소드에서 인덱스 위치는 0부터 시작한다. 따라서, list9.insert(1, 'Alice')는 리스트의 인덱스 1에 'Alice'를 추가한다. 이 경우, 'SP'는 인덱스 0에 위치하고, 'John'은 인덱스 2에 위치하게 된다. 좀 더 쉽게 설명하자면, 'Alice'가 끼게 될 자리인 인덱스 1에 'Alice'가 추가되고, 기존의 요소들은 오른쪽으로 한 칸씩 이동한다. 

#리스트 수정
list10 = ['apple', 'banana', 'cherry'] # 리스트 생성
list10[1] = 'blueberry' # 리스트의 인덱스 1에 있는 요소를 'blueberry'로 수정
print(list10) # ['apple', 'blueberry', 'cherry'] 

# 리스트 인덱스 1에 있는 요소인 'banana'를 'blueberry'로 수정한다. 리스트는 변경 가능한(mutable) 자료형이므로, 인덱스를 이용하여 특정 위치의 요소를 수정할 수 있다. 예를 들어, list10[1] = 'blueberry'는 list10 리스트의 인덱스 1에 있는 요소인 'banana'를 'blueberry'로 변경하여 [ 'apple', 'blueberry', 'cherry']라는 새로운 리스트를 만든다.

# 삭제
#리스트.remove(삭제할요소)
list11 = ['apple', 'banana', 'cherry'] # 리스트 생성
list11.remove('banana') # 리스트에서 'banana' 삭제
print(list11) # ['apple', 'cherry']

# 리스트.pop(인덱스) 리스트에서 제일 마지막에 있는걸 삭제한다. 인덱스를 지정하면 해당 인덱스에 있는 요소를 삭제한다.
list12 = ['apple', 'banana', 'cherry'] # 리스트 생성
list12.pop() # 리스트에서 제일 마지막에 있는 요소 삭제
print(list12) # ['apple', 'banana']
list13 = ['apple', 'banana', 'cherry'] # 리스트 생성
list13.pop(1) # 리스트에서 인덱스 1에 있는 요소 삭제
print(list13) # ['apple', 'cherry']

#리스트.clear() : 리스트의 모든 요소를 삭제한다.
list14 = ['apple', 'banana', 'cherry'] # 리스트 생성
list14.clear() # 리스트의 모든 요소 삭제
print(list14) # []


# 두 리스트를 합치는 방법
list15 = [1, 2, 3] # 리스트 생성
list16 = [4, 5, 6] # 리스트 생성
list17 = list15 + list16 # 두 리스트를 합쳐서 새로운 리스트 생성
print(list17) # [1, 2, 3, 4, 5, 6]
# 리스트.extend(다른리스트) : 리스트에 다른 리스트의 요소를 추가한다.
list18 = [1, 2, 3] # 리스트 생성
list19 = [4, 5, 6] # 리스트 생성
list18.extend(list19) # 리스트에 다른 리스트의 요소 추가
print(list18) # [1, 2, 3, 4, 5, 6]

# +와 extend()의 차이점은, + 연산자는 두 리스트를 합쳐서 새로운 리스트를 생성하는 반면, extend() 메서드는 원래의 리스트에 다른 리스트의 요소를 추가하여 변경한다는 것이다. 따라서, list17은 list15와 list16을 합쳐서 새로운 리스트를 만들지만, list18은 list19의 요소를 list18에 추가하여 변경한다. 리스트를 합칠 때는 + 연산자나 extend() 메서드를 사용할 수 있지만, 원래의 리스트를 변경하지 않고 새로운 리스트를 만들고 싶다면 + 연산자를 사용하는 것이 좋다.


